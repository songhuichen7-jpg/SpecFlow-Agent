class TemplateLibrary:
    """In-memory template catalog for Sprint 3 MCP tools."""

    def __init__(self, assets: tuple[TemplateAsset, ...] = TEMPLATE_ASSETS) -> None:
        self._assets = assets
        self._asset_map = {asset.key: asset for asset in assets}

    def list_templates(self) -> list[dict[str, Any]]:
        return [asset.to_metadata() for asset in self._assets]

    def get_template(self, key: str) -> TemplateAsset:
        try:
            return self._asset_map[key]
        except KeyError as exc:
            raise KeyError(f"Unknown template key: {key}") from exc

    def search_templates(
        self,
        query: str | None = None,
        *,
        category: str | None = None,
    ) -> list[dict[str, Any]]:
        normalized = (query or "").strip().lower()
        tokens = [token for token in normalized.split() if token]
        results: list[tuple[int, TemplateAsset]] = []
        for asset in self._assets:
            if category is not None and asset.category != category:
                continue
            title = asset.title.lower()
            description = asset.description.lower()
            key = asset.key.lower()
            tags = " ".join(asset.tags).lower()
            content = asset.content.lower()
            haystack = " ".join([key, title, description, tags, content])
            if (
                normalized
                and normalized not in haystack
                and any(token not in haystack for token in tokens)
            ):
                continue
            score = 0
            if normalized:
                score += 30 if normalized in title else 0
                score += 20 if normalized in description else 0
                score += 15 if normalized in key else 0
                score += 10 if normalized in tags else 0
            for token in tokens:
                score += 8 if token in title else 0
                score += 5 if token in description else 0
                score += 20 if token in asset.category.lower() else 0
                score += 4 if token in key else 0
                score += 2 if token in tags else 0
            results.append((score, asset))
        results.sort(key=lambda item: (-item[0], item[1].key))
        return [asset.to_metadata() for _, asset in results]

    def get_project_scaffold(self, template_slug: str = DEFAULT_TEMPLATE_SLUG) -> dict[str, str]:
        try:
            return dict(PROJECT_SCAFFOLD_FILES[template_slug])
        except KeyError as exc:
            raise KeyError(f"Unknown project scaffold template: {template_slug}") from exc


def get_default_template_library() -> TemplateLibrary:
    return TemplateLibrary()
