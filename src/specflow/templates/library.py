@dataclass(frozen=True)
class TemplateAsset:
    """A reusable template snippet exposed through MCP template tools."""

    key: str
    category: str
    title: str
    description: str
    tags: tuple[str, ...]
    content: str

    def to_metadata(self) -> dict[str, Any]:
        return {
            "key": self.key,
            "category": self.category,
            "title": self.title,
            "description": self.description,
            "tags": list(self.tags),
        }


